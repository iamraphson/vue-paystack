<template>
    <button v-if="!embed && !triggerClass" v-text="text" class="payButton" @click="payWithPaystack"></button>
    <div v-else-if="embed && !triggerClass" id="paystackEmbedContainer"></div>
</template>

<script type="text/javascript">
  export default {
      props: {
          embed: {
              type: Boolean,
              default: false
          },
          triggerClass: {
              type: String,
              required: false
          },
          paystackkey: {
              type: String,
              required: true
          },
          email: {
              type: String,
              required: true
          },
          amount: {
              type: Number,
              required: true
          },
          reference: {
              type: String,
              required: true
          },
          callback: {
              type: Function,
              required: true,
              default: function(response) {}
          },
          close: {
              type: Function,
              required: true,
              default: function() {}
          },
          text: {
              type: String,
              default: 'Make Payment'
          },
          metadata: {
              type: Object,
              default: function() { return {} }
          },
          currency: {
              type: String,
              default: 'NGN'
          },
          plan: {
              type: String,
              default: ''
          },
          quantity: {
              type: String,
              default: ''
          },
          subaccount: {
              type: String,
              default: ''
          },
          transaction_charge: {
              type: Number,
              default: 0
          },
          bearer: {
              type: String,
              default: ''
          }
      },
      computed: {
          scriptLoaded: function() {
              return new Promise((resolve) => {
                  this.loadScript(() => {
                      resolve()
                  })
              })
          }
      },
      mounted() {
          if (this.embed && !this.triggerClass) {
              this.payWithPaystack()
          } else if (this.triggerClass) {
              document.body.addEventListener('click', _e => {
                  if (_e.target.classList.contains(this.triggerClass)) {
                      this.payWithPaystack()
                  }
              })
          }
      },
      methods: {
          loadScript(callback) {
              const script = document.createElement('script')
              script.src = 'https://js.paystack.co/v1/inline.js'
              document.getElementsByTagName('head')[0].appendChild(script)
              if (script.readyState) {  // IE
                  script.onreadystatechange = () => {
                      if (script.readyState === 'loaded' || script.readyState === 'complete') {
                          script.onreadystatechange = null
                          callback()
                      }
                  }
              } else {  // Others
                  script.onload = () => {
                      callback()
                  }
              }
          },

          payWithPaystack() {
              this.scriptLoaded.then(() => {
                  const paystackOptions = {
                      key: this.paystackkey,
                      email: this.email,
                      amount: this.amount,
                      ref: this.reference,
                      callback: (response) => {
                          this.callback(response)
                      },
                      onClose: () => {
                          this.close()
                      },
                      metadata: this.metadata,
                      currency: this.currency,
                      plan: this.plan,
                      quantity: this.quantity,
                      subaccount: this.subaccount,
                      transaction_charge: this.transaction_charge,
                      bearer: this.bearer
                  }

                  if (this.embed) {
                      paystackOptions.container = 'paystackEmbedContainer'
                  }

                  const handler = window.PaystackPop.setup(paystackOptions)
                  if (!this.embed) {
                      handler.openIframe()
                  }
              })
          }
      }
  }
</script>

